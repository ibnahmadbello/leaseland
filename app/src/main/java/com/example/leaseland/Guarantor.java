package com.example.leaseland;

public class Guarantor {

    private String guarantorFullName;
    private String guarantorPhoneNumber;
    private String guarantorEmail;
    private String guarantorResidentialAddress;

    public String getGuarantorFullName() {
        return guarantorFullName;
    }

    public void setGuarantorFullName(String guarantorFullName) {
        this.guarantorFullName = guarantorFullName;
    }

    public String getGuarantorPhoneNumber() {
        return guarantorPhoneNumber;
    }

    public void setGuarantorPhoneNumber(String guarantorPhoneNumber) {
        this.guarantorPhoneNumber = guarantorPhoneNumber;
    }

    public String getGuarantorEmail() {
        return guarantorEmail;
    }

    public void setGuarantorEmail(String guarantorEmail) {
        this.guarantorEmail = guarantorEmail;
    }

    public String getGuarantorResidentialAddress() {
        return guarantorResidentialAddress;
    }

    public void setGuarantorResidentialAddress(String guarantorResidentialAddress) {
        this.guarantorResidentialAddress = guarantorResidentialAddress;
    }

}
